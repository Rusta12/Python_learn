# piu_piu

# мессанджер который работает в постоянном диалоге с пользователем
# есть несколько режимов работы (web, native, console)
# содержит следующий функционал:
# - регистрация
# - авторизация с двойной аутентификацией
# - поиск пользователей
# - отправка сообщений (сообщения храняться на машине пользователя)
# - просмотр сообщений
# - создание групп
# - отправка сообщений всем участникам группы
# - вступление в группы
# - возможность получение системных уведомлений
# - возможность получение почтовых уведомлений
# - распечатка сообщений в формате xlsx, txt, bash
# - автоматическое формирование консольного профиля с 
#	произвольной аватаркой основанной на логине
# - просмотр своего и чужого профиля
# - вызов помощи

