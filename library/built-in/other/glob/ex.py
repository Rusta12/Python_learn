from glob import *

# :::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

def _main():
	glob('./[0-9].*') 	# ['./1.gif', './2.txt']
	glob('*.gif') 		# ['1.gif', 'card.gif']
	glob('?.gif')		# ['1.gif']
	glob('**/*.txt', recursive=True)	# ['2.txt', 'sub/3.txt']
	glob('./**/', recursive=True)		# ['./', './sub/']


# :::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

if __name__ == '__main__':
	_main()
