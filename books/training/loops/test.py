#	zip() ::::::::::::::::::::::::::::::::::::::::::::::::::::::

#	ex1 ..........................
L1 = [1, 2, 3, 4]
L2 = [5, 6, 7, 8]

for (x, y) in zip(L1, L2):
	print(x, '+', y, '=', x + y)

#	ex2 ..........................
E1 = ['a', 'b', 'c'] 	# keys
E2 = [1, 2, 3] 			# values
d = {}					# dict

for (x, y) in zip(E1, E2): d[x] = y

print(
	dict(zip(E1, E2)), 
	open('test.py', 'a')
	)